#include "libft.h"

char	*ft_itoa(int n)
{
	char	num[11];
	int		i;

	if (n == INT_MIN)
		return (ft_strdup("-2147483648"));
	num[0] = '+';
	if (n < 0)
	{
		num[0] = '-';
		n = -n;
	}
	i = 10;
	while (n > 9)
	{
		num[i] = (n % 10) + '0';
		n = n / 10;
		i--;
	}
	num[i] = n + '0';
	if (num[0] == '-' && i > 0)
	{
		i--;
		num[i] = num[0];
	}
	return (ft_strndup(&num[i], 11 - i));
}
